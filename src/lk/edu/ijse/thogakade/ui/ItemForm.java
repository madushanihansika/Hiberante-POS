    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            ItemController itemController = new ItemControllerImpl();

            ItemDTO itemDTO = new ItemDTO();
            itemDTO.setItemId(txtItemId.getText());
            itemDTO.setDescription(txtItemName.getText());
            itemDTO.setQty_on_hand(Integer.parseInt(txtItemQty.getText()));
            itemDTO.setUnitprice(Double.parseDouble(txtUnitPrice.getText()));

            boolean isAdded = itemController.saveItem(itemDTO);
            if (isAdded) {
                System.out.println("Added Succes");
                LoadItem();
            } else {
                System.out.println("Added Fail");
            }
        } catch (Exception ex) {
            Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            ItemController itemController = new ItemControllerImpl();
            String id = txtItemId.getText();
            boolean isDelete = itemController.deleteItem(id);
            if (isDelete) {
                JOptionPane.showMessageDialog(this, "Delete Success");
                txtItemId.setText("");
                txtItemName.setText("");
                txtItemQty.setText("");
                txtUnitPrice.setText("");
                LoadItem();
            }else{
                JOptionPane.showMessageDialog(this, "Delete Fail");
            }
        } catch (Exception ex) {
            Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

