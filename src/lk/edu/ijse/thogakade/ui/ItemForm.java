    private void LoadItem() {
        try {
            ItemController itemController = new ItemControllerImpl();
            dtm = (DefaultTableModel) tblItem.getModel();
            dtm.setRowCount(0);
            List<ItemDTO> itemdtos =itemController.viewItems();
            for (ItemDTO item1 : itemdtos) {
                Object[] rowdata = {item1.getItemId(), item1.getDescription(), item1.getQty_on_hand(), item1.getUnitprice()};
                dtm.addRow(rowdata);
            }
        } catch (Exception ex) {
            Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
